#!/usr/bin/env python3
# In Python, the data type is set when you assign a value to a variable.

var_str = "Hello"               # str
var_int = 20                    # int
var_float = 20.5                # float
var_list = ["apple", "banana"]  # list (ordered, changeable)
var_tuple = ("apple", "banana") # tuple (ordered, unchangeable)
var_dict = {"name": "John"}     # dict (key-value pairs)
var_bool = True                 # bool (Boolean)

# You can get the data type of any object by using the type() function:
print("Type of var_str:", type(var_str))
print("Type of var_int:", type(var_int))
print("Type of var_list:", type(var_list))
print("Type of var_bool:", type(var_bool))
