#!/usr/bin/env python3

# math.py
import math

print("--- Built-in Python Math Functions ---")
print(f"Minimum of (5, 10, 25): {min(5, 10, 25)}")
print(f"Maximum of (5, 10, 25): {max(5, 10, 25)}")
print(f"Absolute value abs(-7.25): {abs(-7.25)}")
print(f"Power pow(4, 3): {pow(4, 3)}")

print("\n--- Functions from the 'math' Module ---")
print(f"Square root of 64: {math.sqrt(64)}")
print(f"Ceil value of 1.4 (round up): {math.ceil(1.4)}")
print(f"Floor value of 1.4 (round down): {math.floor(1.4)}")
print(f"Value of Pi constant: {math.pi}")
